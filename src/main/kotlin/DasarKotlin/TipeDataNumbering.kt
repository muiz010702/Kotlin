package DasarKotlin

fun main() {
    //tipe data Number
    var Luas: Int = 200
    println(Luas)
    var Nilai: Byte = 77
    println(Nilai)
    var Panjang: Long = 800000
    println(Panjang)
    var Jarak: Short = 8000
    println(Jarak)

    //Tipe Data Floating Point
    var sample: Float = 99.90F
    println(sample)
    var Jumlah: Double = 99.2198
    println(Jumlah)
}